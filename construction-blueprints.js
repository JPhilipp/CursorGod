ConstructionBlueprints.prototype.getBlocks = function(type) {
    var blocks = [];
    var blockSizeZ = this.blockSize;
    var dataForType = this.data[type];
    if (dataForType) {
        var blocksByFloor = dataForType.blocksByFloor;
        if (dataForType.multiplierZ) { blockSizeZ *= dataForType.multiplierZ; }
        
        for (var zI = 0; zI < blocksByFloor.length; zI++) {
            var floorBlocksByY = blocksByFloor[zI];
            for (var yI = 0; yI < floorBlocksByY.length; yI++) {
                var floorBlocksByXString = floorBlocksByY[yI];
                var floorBlocksByX = floorBlocksByXString.split(' ');
                for (var xI = 0; xI < floorBlocksByX.length; xI++) {

                    var propertiesIndex = floorBlocksByX[xI];
                    if (propertiesIndex != this.charForEmpty) {
                        var x = this.blockSize * xI - (floorBlocksByX.length * this.blockSize / 2);
                        var y = (floorBlocksByY.length * this.blockSize) -
                                (this.blockSize * yI) - (floorBlocksByY.length * this.blockSize / 2);
                        var z = (blocksByFloor.length * blockSizeZ) -
                                (blockSizeZ * zI) - (blocksByFloor.length * blockSizeZ / 2);
                        
                        if (dataForType.isLower) {
                            z -= .15 * (dataForType.multiplierZ ? dataForType.multiplierZ : 1);
                        }
                        if (dataForType.multiplierZ) {
                            z += dataForType.multiplierZ / 2;
                        }

                        var block = {};
                        block.position = new THREE.Vector3(x, y, z);
                        block.properties = Misc.cloneObject(dataForType.properties[propertiesIndex]);
                        if (dataForType.multiplierZ) {
                            block.properties.multiplierZ = dataForType.multiplierZ;
                        }
                        blocks.push(block);
                    }

                }
            }
        }
    }

    return blocks;
};
